    Screen('DrawText',w,'0',...
            psych_center(1) - bnd(3)/2,...
            psych_center(2) - bnd(4)/2,...
            params.frame_colors(data.frame(trial),:));
    % Right zoo animal
    Screen('DrawText',w,'0',...
            psych_right(1) - bnd(3)/2,...
            psych_right(2) - bnd(4)/2,...
            params.frame_colors(data.frame(trial),:));
